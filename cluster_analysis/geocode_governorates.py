def geocode(name, region):
    q = f"{name}، {region}، المملكة العربية السعودية"
    url = "https://maps.googleapis.com/maps/api/geocode/json?" + urllib.parse.urlencode(
        {"address": q, "region": "sa", "language": "ar", "key": KEY})
    for attempt in range(4):
        try:
            with urllib.request.urlopen(url, timeout=20) as r:
                j = json.load(r)
            if j["status"] == "OK":
                loc = j["results"][0]["geometry"]["location"]
                return round(loc["lat"], 5), round(loc["lng"], 5)
            if j["status"] == "ZERO_RESULTS":
                return None
            raise RuntimeError(j.get("error_message", j["status"]))
        except Exception as e:
            if attempt == 3:
                print("  !", name, e); return None
            time.sleep(2 ** attempt)

records = []
fail = []
for region, d in GOVS.items():
    items = [(d["مقر"], "مقر")] + [(n, "أ") for n in d["أ"]] + [(n, "ب") for n in d["ب"]]
    for name, cat in items:
        ll = geocode(name, region)
        time.sleep(0.05)
        if ll is None:
            fail.append((region, name))
            continue
        records.append({"name": name, "region": region, "cat": cat,
                        "lat": ll[0], "lon": ll[1]})
    print(f"{region}: {len([1 for r in records if r['region']==region])} نقطة")

print("\nإجمالي:", len(records), "| فشل:", fail)
